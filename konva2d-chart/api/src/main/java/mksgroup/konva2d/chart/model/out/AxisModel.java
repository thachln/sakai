package mksgroup.konva2d.chart.model.out;

/**
 * @author lengocthach
 *
 */
public class AxisModel {
    private Double minX;
    private Double maxX;
    private Double minY;
    private Double maxY;
    private Double unitsPerTickX;
    private Double unitsPerTickY;

    /**
     * @return the minX
     */
    public Double getMinX() {
        return minX;
    }

    /**
     * @param minX
     *            the minX to set
     */
    public void setMinX(Double minX) {
        this.minX = minX;
    }

    /**
     * @return the maxX
     */
    public Double getMaxX() {
        return maxX;
    }

    /**
     * @param maxX
     *            the maxX to set
     */
    public void setMaxX(Double maxX) {
        this.maxX = maxX;
    }

    /**
     * @return the minY
     */
    public Double getMinY() {
        return minY;
    }

    /**
     * @param minY
     *            the minY to set
     */
    public void setMinY(Double minY) {
        this.minY = minY;
    }

    /**
     * @return the maxY
     */
    public Double getMaxY() {
        return maxY;
    }

    /**
     * @param maxY
     *            the maxY to set
     */
    public void setMaxY(Double maxY) {
        this.maxY = maxY;
    }

    /**
     * @return the unitsPerTickX
     */
    public Double getUnitsPerTickX() {
        return unitsPerTickX;
    }

    /**
     * @param unitsPerTickX
     *            the unitsPerTickX to set
     */
    public void setUnitsPerTickX(Double unitsPerTickX) {
        this.unitsPerTickX = unitsPerTickX;
    }

    /**
     * @return the unitsPerTickY
     */
    public Double getUnitsPerTickY() {
        return unitsPerTickY;
    }

    /**
     * @param unitsPerTickY
     *            the unitsPerTickY to set
     */
    public void setUnitsPerTickY(Double unitsPerTickY) {
        this.unitsPerTickY = unitsPerTickY;
    }
}
