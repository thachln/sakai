package m.k.s.eng.sakai.model.vocabulary;

public class QuestionContentVocabulary {
    private String image;
    private String audio;
    private String justText;
    /**
     * Get value of image.
     * @return the image
     */
    public String getImage() {
        return image;
    }
    /**
     * Get value of audio.
     * @return the audio
     */
    public String getAudio() {
        return audio;
    }
    /**
     * Get value of justText.
     * @return the justText
     */
    public String getJustText() {
        return justText;
    }
    /**
     * Set the value for image.
     * @param image the image to set
     */
    public void setImage(String image) {
        this.image = image;
    }
    /**
     * Set the value for audio.
     * @param audio the audio to set
     */
    public void setAudio(String audio) {
        this.audio = audio;
    }
    /**
     * Set the value for justText.
     * @param justText the justText to set
     */
    public void setJustText(String justText) {
        this.justText = justText;
    }
}
